day_number = int(input("Введіть номер дня тижня (1-7): "))

days_of_week = ["понеділок", "вівторок", "середа", "четвер", "п'ятниця", "субота", "неділя"]


if 1 <= day_number <= 7:
    print(f"День тижня з номером {day_number}: {days_of_week[day_number - 1]}")
else:
    print("Некоректний ввід. Введіть номер дня від 1 до 7.")